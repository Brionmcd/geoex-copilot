// Helper: get workflows by category
export function getWorkflowsByCategory(categoryId: string): WorkflowDefinition[] {
  return workflows.filter((w) => w.categoryId === categoryId);
}

// Helper: get featured workflows for quick-access chips
export function getFeaturedWorkflows(): WorkflowDefinition[] {
  return workflows.filter((w) => w.isFeatured);
}

// Helper: find a workflow by its prompt (for matching from SuggestedPrompts)
export function findWorkflowByPrompt(prompt: string): WorkflowDefinition | undefined {
  return workflows.find((w) => w.prompt === prompt);
}

// Helper: get workflow stats
export function getWorkflowStats() {
  return {
    total: workflows.length,
    automated: workflows.filter((w) => w.status === "automated").length,
    planned: workflows.filter((w) => w.status === "planned").length,
    inReview: workflows.filter((w) => w.status === "in_review").length,
  };
}
