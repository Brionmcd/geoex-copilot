// ── Computed helpers ──

export function getActivePax(dep: GroupDeparture): number {
  return dep.guests.filter(
    (g) => g.status !== "Cancelled" && g.status !== "Transferred Out"
  ).length;
}

export function getFillPercentage(dep: GroupDeparture): number {
  return Math.round((getActivePax(dep) / dep.maxCapacity) * 100);
}

export function getExpectedAvailabilityLanguage(dep: GroupDeparture): string {
  const fill = getFillPercentage(dep);
  const remaining = dep.maxCapacity - getActivePax(dep);
  if (fill <= 50) return "Open";
  if (fill <= 75) return "Limited Availability";
  if (fill <= 90) return `Only ${remaining} Spots Remaining`;
  if (fill < 100) return `Last ${remaining} Spots!`;
  if (fill === 100) return "Sold Out";
  return "Waitlist Open";
}

export function getCorrectTierIndex(dep: GroupDeparture): number {
  const pax = getActivePax(dep);
  for (let i = dep.pricingTiers.length - 1; i >= 0; i--) {
    if (pax >= dep.pricingTiers[i].minPax) return i;
  }
  return 0;
}

export function getRevenueBooked(dep: GroupDeparture): number {
  return dep.guests
    .filter((g) => g.status !== "Cancelled" && g.status !== "Transferred Out")
    .reduce((sum, g) => sum + g.landPriceInvoiced, 0);
}

export function getPaymentsCollected(dep: GroupDeparture): number {
  return dep.guests
    .filter((g) => g.status !== "Cancelled" && g.status !== "Transferred Out")
    .reduce((sum, g) => sum + g.payments.totalPaid, 0);
}
